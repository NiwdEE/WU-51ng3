import numpy as np

def generate_matrix(L, taps):
    A = np.zeros((L,L), dtype=np.uint8)
    for i in range(1, L):
        A[i, i-1] = 1

    for j in taps:
        A[0, j] = 1
    
    return A

def get_step(init, taps, step):
    L = len(init)
    A = generate_matrix(L, taps)
    A_n = np.linalg.matrix_power(A, step) % 2
    return (A_n @ init) % 2

